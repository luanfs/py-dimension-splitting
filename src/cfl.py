####################################################################################
#
# Module for computing the CFL number
####################################################################################
import numpy as np


####################################################################################
# CFL number in x direction
####################################################################################
def cfl_x(u_edges, simulation):
    cx = u_edges*simulation.dt/simulation.dx
    return cx

####################################################################################
# CFL number in x direction
####################################################################################
def cfl_y(v_edges, simulation):
    cy = v_edges*simulation.dt/simulation.dy
    return cy
